	public static void DrawImage (Graphics g, String str, int x, int y)
	{
		int n = str.length();
		int imageWidth = 0;				
		
		for(int i=0;i<n;i++)
		{			
			int index = GetIndex(str.charAt(i));
			if(index!=-1)
				imageWidth += RectList[index][2];
			else
				imageWidth += 9;
		}		
		
		int xCur = x;
		int yCur = y;
	
		for(int i=0;i<n;i++)
		{
			int index = GetIndex(str.charAt(i));
			if(index!=-1)
			{
				int _x = RectList[index][0];
				int _y = RectList[index][1];
				int width = RectList[index][2];
				int height = RectList[index][3];			
				
				g.setClip(xCur, yCur, width, height);
				g.drawRegion(m_imgCharImage, _x, _y, width, height, 
							Sprite.TRANS_NONE, xCur, yCur, Graphics.TOP|Graphics.LEFT);
				
				xCur += 1 + width;
			}
			else			
				xCur += 9;
		}
	}
	
	private static int GetIndex (char c)
	{
		int n = CharList.length;
		for(int i=0;i<n;i++)
		{						
			if(CharList[i]==c)
				return i;
		}
		return -1;
	}
